/** The type of object returned by `JSONSerialization.jsonObject()`
 to represent a JSON array. */
public typealias JSONArray = [Any]

/** The type of object returned by `JSONSerialization.jsonObject()`
 to represent a JSON dictionary. */
public typealias JSONDictionary = [String: Any]
